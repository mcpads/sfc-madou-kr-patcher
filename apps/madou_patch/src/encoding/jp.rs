/// Build byte→char lookup for single-byte characters (256 slots).
pub fn build_decode_table() -> [Option<char>; 256] {
    let mut table = [None; 256];
    for &(byte, ch) in SINGLE_BYTE_TABLE {
        table[byte as usize] = Some(ch);
    }
    table
}

/// Build byte→char lookup for FB-prefix characters (256 slots).
pub fn build_fb_decode_table() -> [Option<char>; 256] {
    let mut table = [None; 256];
    for &(idx, ch) in FB_PREFIX_TABLE {
        table[idx as usize] = Some(ch);
    }
    table
}

/// Build char→bytes reverse lookup for JP encoding.
#[allow(dead_code)]
pub fn build_encode_table() -> Vec<(char, Vec<u8>)> {
    let mut table = Vec::new();
    for &(byte, ch) in SINGLE_BYTE_TABLE {
        table.push((ch, vec![byte]));
    }
    for &(idx, ch) in FB_PREFIX_TABLE {
        table.push((ch, vec![0xFB, idx]));
    }
    table
}

